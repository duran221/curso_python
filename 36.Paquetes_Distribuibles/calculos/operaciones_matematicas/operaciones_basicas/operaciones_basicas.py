def sumar(num1,num2):
    print(f"El resultado de la operacion es: {num1+num2}")


def restar(num1,num2):
    print(f"El resultado de la operacion es: {num1-num2}")


def multiplicar(num1,num2):
    print(f"El resultado de la operacion es: {num1*num2}")


def dividir(num1,num2):
    print(f"El resultado de la operacion es: {num1/num2}")